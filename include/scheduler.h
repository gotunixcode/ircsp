#ifndef		__INCLUDED_SCHEDULER_H__
#define		__INCLUDED_SCHEDULER_H__


/*
---------------------------------------------------------------------------------------------------------------------------------
    STRUCTURES
---------------------------------------------------------------------------------------------------------------------------------
*/
typedef struct _scheduler
{
	char			*task_name;		/* Name of the task */
	time_t			task_last_run;		/* Last time the task was run */
	int			task_interval;		/* Time in seconds before we run again */
	int			(*func)(void);		/* Function we run for this task */

	struct _scheduler	*next;			/* Pointer to next record on the Scheduler List */
} scheduler_t;


scheduler_t			*scheduler_h;


/*
---------------------------------------------------------------------------------------------------------------------------------
    PROTOTYPES
---------------------------------------------------------------------------------------------------------------------------------
*/
scheduler_t		*Scheduler_add			(char *, int, int (*func)(void));
scheduler_t		*Scheduler_find			(char *);
void			Scheduler_del			(char *);


#endif		/* __INCLUDED_SCHEDULER_H__ */
